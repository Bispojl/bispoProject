"""""
Tipos int e float
int -> numeros inteiros.
o tipo int representa qualquer numero.
positivo ou negativo. int sem sinal é considerado positivo.
"""
print(11, 2)
print(2, 0, -100)

# float -> numero com ponto flutuante.
# o tipo float representa qualquer numero positivo ou negativo com ponto flutuante (casa decimal).
#float sem sinal é considerado positivo.
print(0.0, 8.9)
print(-2.9, 4.9, -200)

#A classe type mostra o tipo que o python inferiu o valor.
